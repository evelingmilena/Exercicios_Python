def horas(time):
    '''
    Recebe uma lista time contendo [hh, mm, ss]
    Retorna a hora (hh).
    '''
    return time[0]
    
def minutos(time):
    '''
    Recebe uma lista time contendo [hh, mm, ss]
    Retorna a minutos (mm).
    '''
    return time[1]
    
def segundos(time):
    '''
    Recebe uma lista time contendo [hh, mm, ss]
    Retorna os segundos (ss).
    '''
    return time[2]
    

