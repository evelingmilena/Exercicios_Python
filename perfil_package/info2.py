def endereco_pessoa(logradouro, numero, bairro, cidade, estado):
    '''
    Endereco deve ser da forma: 
    input: logradouro (rua, avenida, etc), numero, bairro, cidade, estado, pais
    output: lista com esses dados
    '''
    endereco = {"logradouro": logradouro,"numero":numero,"bairro":bairro,"cidade":cidade,"estado":estado}
    return endereco
    

    
